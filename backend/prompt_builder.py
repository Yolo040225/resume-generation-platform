def build_prompt(resume_text: str, job_profile: dict, job_title: str, job_jd: str) -> str:
    skills = ", ".join(job_profile.get("skills", []))

    prompt = f"""
    你是一名顶级互联网大厂的高级HR和资深面试官，非常擅长将求职者平庸的简历进行“脱胎换骨”的重写。

    你的任务是：根据【目标岗位要求】，对【原始简历】进行深度重写和亮点挖掘。

    ==============================
    【目标岗位】
    {job_title}

    【岗位要求与特征】
    类型：{job_profile.get("job_category", "")}
    核心技能：{skills}
    学历要求：{job_profile.get("education_level", "")}
    经验要求：{job_profile.get("experience_years", "")}

    【岗位描述(JD)详情】
    {job_jd}

    ==============================
    【原始简历】
    {resume_text}

    ==============================
    【高阶重写规则（必须严格遵守）】
    1. 真实性底线：绝对不得编造任何不存在的学历、公司、项目或技能！只能基于原始信息进行重构。
    2. 匹配度拉满：将原始经历中的描述，尽量往目标岗位的【核心技能】和【JD描述】上靠拢，使用相关的专业术语替换口语化表达。
    3. 🌟 STAR 法则重写：对于“项目/实习经历”的描述（desc），必须使用 STAR 法则进行重写（情境-任务-行动-结果）。
       - 动作化：多用“负责”、“主导”、“优化”、“实现了”等动词开头。
       - 数据化：如果原简历有数据，强化数据；如果没有，用“显著提升”、“大幅优化”等结果导向的词汇结尾。
       - 🚫【格式红线】：绝对不要在描述中写出“情境：”、“任务：”、“行动：”、“结果：”等任何前缀标签！必须将逻辑无缝融合进连贯的专业语句中。
    4. 💡 重点高亮提取：在重写项目描述（desc）时，对于你为了迎合目标岗位 JD 而特意加入的专业术语、核心技能词汇，必须使用 <b> 标签进行包裹（例如：<b>协同过滤算法</b>、<b>QPS提升了50%</b>）。

    【Few-Shot 示范（学习这种重写风格）】
    原简历描述：做了一个学生管理系统，用了Java和MySQL，可以增删改查。
    重写后描述：基于 Java 与 MySQL 主导开发学生信息管理系统，设计并优化底层数据库表结构，实现百万级数据的毫秒级增删改查，显著提升教务系统的数据处理效率。

    ==============================
    【输出格式要求（最高优先级）】
    你必须且只能输出一个合法的 JSON 格式字符串，不要输出任何额外的问候语、分析过程或 Markdown 代码块标记（如 ```json）。

    ⚠️⚠️⚠️ JSON 格式安全警告（极其重要） ⚠️⚠️⚠️
    1. 绝不允许使用真实的换行符：JSON 字符串的值中，绝对不能敲回车换行！如果需要分段，必须且只能使用连续的字符串并用 `<br>` 标签隔开。
    2. 绝不允许使用未转义的双引号：JSON 字符串内部的词语强调，请一律替换为单引号（'）或者中文引号（“”），绝对不能破坏外层的双引号结构！
    3. 必须闭合括号：请确保所有的 {{ 和 }}，以及 [ 和 ] 都完美配对。

    JSON 结构必须严格如下：

    {{
        "personal_info": {{
            "name": "姓名（如果原始简历没有则填'未提供'）",
            "contact": "电话/邮箱（如果原始简历没有则填'未提供'）",
            "intent": "{job_title}"
        }},
        "educations": [
            {{"time": "2022-2026", "school": "XX大学", "major": "专业", "note": "提炼出与岗位相关的课程或成绩"}}
        ],
        "experiences": [
            {{"time": "2024", "title": "项目/实习名称", "role": "角色", "desc": "• 运用STAR法则重写后的要点1<br>运用STAR法则重写后的要点2"}}
        ],
        "skills": ["把原简历的技能结合JD的核心技能，整理成专业词汇"]
    }}
    """
    return prompt